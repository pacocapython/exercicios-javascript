//3. Status do Sistema (Operador Ternário)
//Crie uma variável statusSistema = 1. Use o operador ternário (? :) para guardar em uma variável mensagem
//  o texto "Sistema Ativo" se o status for 1, ou "Sistema Inativo" se for qualquer outro valor. Exiba mensagem no console.
const statusSistema = 1;
let mensagem = statusSistema === 1 ? "Sistema Ativo" : "Sistema Inativo";
console.log(mensagem);
